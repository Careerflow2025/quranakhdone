// Function to get Quran text by script and surah
export function getQuranByScript(scriptId: string, surahNumber: number) {
  const script = quranData[scriptId as keyof typeof quranData];
  if (!script) return null;
  
  const surah = script.surahs[surahNumber as keyof typeof script.surahs];
  return surah || null;
}

// Get all available scripts
export function getAllScripts() {
  return Object.keys(quranData).map(key => ({
    id: key,
    name: quranData[key as keyof typeof quranData].name,
    nameArabic: quranData[key as keyof typeof quranData].nameArabic,
    description: quranData[key as keyof typeof quranData].description
  }));
}